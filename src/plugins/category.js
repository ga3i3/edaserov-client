export default getCategoryName = (name) => {
	if (name == 'hotter') {
		return "Горячее";
	}
	if (name == 'laych') {
		return "Бизнес ланч";
	}
	if (name == 'salads') {
		return "Салаты";
	}
	if (name == 'soups') {
		return "Супы";
	}
	if (name == 'desserts') {
		return "Десерты";
	}
	if (name == 'wok') {
		return "WOK";
	}
}